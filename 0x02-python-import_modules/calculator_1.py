#!/usr/bin/python3

def add(a, b):
    """Addition function

    Args:
    a: first integer
    b: first integer

    Returns:
    the return value. a + b

"""
    return (a + b)

def sub(a, b):
    """Subtraction function

    Args:
    a: first integer
    b: second integer

    Returns:
    the return value. a - b
"""
    return (a -b)

def mul(a, b):
    """multiplication function

    Args:
    a: first integer
    b: second integer

    Returns:
    the return value. a * b
"""
    return (a * b)

def div(a, b):
    """division function

Args:
    a: first integer
    b: second integer

    Returns:
    the return value. a / b
"""
    return int(a / b)
